% --- Executes on button press in pushbutton20.
function pushbutton20_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton20 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

if ~isempty(handles.last_path)    
    [filename,filepath] = uiputfile({'*.txt','Text files(*.txt)';'*.*','All files(*.)'},...
        'Save Values',handles.last_path);
    handles.last_path = filepath;
else
     [filename,filepath] = uiputfile({'*.txt','Text files(*.txt)';'*.*','All files(*.)'},...
        'Save Values');
    handles.last_path = filepath;
end

if filepath==0
    return
end

fid = fopen([filepath filename],'w');
fwrite(fid,'');
%%%Making possible for the users to be able to write only the variablesh that
%they need.
% varsinworks = workSpaceVars('chi','pha','is');
varsinworks = evalin('base', 'who(''chi'',''pha'',''is'')');
abbr = cell(1,length(varsinworks));
for c = 1:length(varsinworks)
    abbr{c}=varsinworks{c};
end
%%celldisp(abbr)
%%%%

count_abbr = 1;
while count_abbr <= length(abbr)
    sub = fieldnames(evalin('base',sprintf('%s',abbr{count_abbr})));
    count_sub = 1;
    while count_sub <= length(sub)
        val = evalin('base',sprintf('%s.%s',abbr{count_abbr},sub{count_sub}));
        fwrite(fid,sprintf('%s.%s=%d\r\n',abbr{count_abbr},sub{count_sub},val));
        count_sub = count_sub+1;
    end
    count_abbr= count_abbr+1;
end
fclose(fid);
savedName = filename(1:regexpi(filename,'\.')-1);
set(handles.text80,'String',...
    sprintf('Your values have been succesfully saved in the textfile \n%s\n\n in location \n%s',savedName,filepath));




% --- Executes on button press in pushbutton21.
function pushbutton21_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton21 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
if ~isempty(handles.last_path)
    [file,pathName] = uigetfile({'*.txt','Text files(*.txt)';'*.*','All files(*.*)'},...
    'Select Values','multiSelect','off',handles.last_path);
    handles.last_path = pathName;
else
    [file,pathName] = uigetfile({'*.txt','Text files(*.txt)';'*.*','All files(*.)'},...
    'Select Values','multiSelect','off');  
     handles.last_path = pathName;
end

if pathName == 0 
    return
end

fid = fopen([pathName file],'r');
line = fgetl(fid);
while line~=-1
    if isempty(line) || strcmp(line(1),'*')
        while isempty(line) || strcmp(line(1),'*');
            line = fgetl(fid);
        end
    else
        lineSplit = regexp(line,'\=','split');
        lineSplit1 = regexp(lineSplit{1},'\.','split');
        lineSplit2 = char(lineSplit1(2));
        lineSplit1 = char(lineSplit1(1));
        lineSplit3 = str2double(char(lineSplit(2)));
        evalin('base',sprintf('%s.%s=%d;',lineSplit1,lineSplit2,lineSplit3));
        line = fgetl(fid);
        while isempty(line) || strcmp(line(1),'*');
            line = fgetl(fid);
        end
    end
end


   


